    #[test]
    fn test_retransmit_syn() {
        let mut client = TcpConnection::with_capacity(client_id(), 1024);
        let t0 = Instant::from_millis(0);

        let syn_out = client.connect(1000, t0).unwrap();
        assert!(!client.retransmit_queue_empty());

        // Before timeout → nothing to retransmit
        let t_early = Instant::from_millis(500);
        let segs = client.poll_retransmit(t_early).unwrap();
        assert!(segs.is_empty());

        // At timeout (initial RTO = 1000 ms) → SYN retransmitted
        let t_rto = Instant::from_millis(1000);
        let segs = client.poll_retransmit(t_rto).unwrap();
        assert_eq!(segs.len(), 1);
        assert_eq!(segs[0].seq, syn_out.seq);
        assert_eq!(segs[0].flags, flags::SYN);
    }

    #[test]
    fn test_retransmit_data_segment() {
        let (mut client, _server) = establish_pair();
        let t_send = Instant::from_millis(100);

        // Send data
        client.send(b"test data").unwrap();
        let data_seg = client.flush_send(t_send).unwrap();
        assert!(!client.retransmit_queue_empty());

        // RTO is initially 1000ms; after handshake RTT samples it may
        // have been updated, but let's check at a safe time well past RTO.
        let t_timeout = Instant::from_millis(2000);
        let segs = client.poll_retransmit(t_timeout).unwrap();
        assert_eq!(segs.len(), 1);
        assert_eq!(segs[0].seq, data_seg.seq);
        assert_eq!(segs[0].payload, b"test data");
    }

    #[test]
    fn test_ack_clears_retransmit_queue() {
        let (mut client, mut server) = establish_pair();
        let t = Instant::from_millis(100);

        // Send data and get ACK
        client.send(b"acked data").unwrap();
        let data_seg = client.flush_send(t).unwrap();
        assert!(!client.retransmit_queue_empty());

        // Server receives and ACKs
        let data_bytes = seg_to_bytes(&data_seg, &[10, 0, 0, 1], &[10, 0, 0, 2]);
        let data_pkt = TcpPacket::new(&data_bytes[..]).unwrap();
        let ack_out = server.on_segment(&data_pkt, t).unwrap().unwrap();

        // Client processes the ACK
        let ack_bytes = seg_to_bytes(&ack_out, &[10, 0, 0, 2], &[10, 0, 0, 1]);
        let ack_pkt = TcpPacket::new(&ack_bytes[..]).unwrap();
        let t_ack = Instant::from_millis(120);
        client.on_segment(&ack_pkt, t_ack).unwrap();

        // Retransmit queue should be empty
        assert!(client.retransmit_queue_empty());
    }

    #[test]
    fn test_retransmit_backoff() {
        let mut client = TcpConnection::with_capacity(client_id(), 1024);
        let t0 = Instant::from_millis(0);
        client.connect(1000, t0).unwrap();

        // First RTO fires at t=1000, backed_off_rto doubles to 2000
        let segs = client.poll_retransmit(Instant::from_millis(1000)).unwrap();
        assert_eq!(segs.len(), 1);

        // Second RTO should NOT fire at t=2000 (need 1000 + 2000 = 3000 from last_sent_at=1000)
        let segs = client.poll_retransmit(Instant::from_millis(2000)).unwrap();
        assert!(segs.is_empty());

        // Second RTO fires at t=3000
        let segs = client.poll_retransmit(Instant::from_millis(3000)).unwrap();
        assert_eq!(segs.len(), 1);
    }

    #[test]
    fn test_next_retransmit_at() {
        let mut client = TcpConnection::with_capacity(client_id(), 1024);
        let t0 = Instant::from_millis(0);

        // No outstanding segments → None
        assert!(client.next_retransmit_at().is_none());

        client.connect(1000, t0).unwrap();
        // Initial RTO = 1000ms, sent at t=0 → next retransmit at t=1000
        assert_eq!(client.next_retransmit_at(), Some(Instant::from_millis(1000)));
    }
